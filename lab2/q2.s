	AREA reset,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0,=SRC+(N)*4
	LDR R1,=SAI+(N)*4
	LDR R2,=DST+(N)*4
	LDR R3,=0x00000000
	LDR R10,=0x00000100
	MOV R4,#4
	
LOOP
	ADCS R3,R0,R1
	LDR R10,[R0],#-4
	LDR R10,[R1],#-4
	STR R3,[R2]
	LDR R10,[R2],#-4
	SUBS R4,#1
	BNE LOOP
STOP
	B STOP
N EQU 3
SRC DCD 0x00000010,0x00000011,0x00000012,0x00000013
SAI DCD 0x00000020,0x00000021,0x00000022,0x00000023
	AREA result,DATA,READWRITE
DST DCD 0x0
	END