	AREA reset,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	EXPORT Reset_Handler
	ENTRY
Reset_Handler
	LDR R0,=SRC
	LDR R1,=SRC1
	LDR R10,=DST
	LDMIA R0,{R2-R5}
	LDMIA R1,{R6-R9}
	ADDS R5,R5,R9
	ADCS R4,R4,R8
	ADCS R3,R3,R7
	ADCS R2,R2,R6
	STMIA R10,{R2-R5}
STOP
	B STOP
SRC DCD 0x00000001,0x00000002,0x00000003,0x00000004
SRC1 DCD 0x00000001,0x00000002,0x00000003,0x00000004
	AREA result,DATA,READWRITE
DST DCD 0x0
	
	END