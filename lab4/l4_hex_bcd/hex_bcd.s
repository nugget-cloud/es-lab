	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000 
	DCD Reset_Handler 
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R1,=NUM
	LDR R2,=RESULT
	MOV R6,#0x0A
	LDRB R3,[R1]
	MOV R4,#0
LOOP
	SUB R3,R6
	ADD R4,#1
	CMP R3,R6
	BCS LOOP
	LSL R5,R4,#4
	ADD R5,R3
	STRB R5,[R2]
NUM DCD 0x0000060
	AREA data, DATA, READWRITE
RESULT DCD 0
	END
 